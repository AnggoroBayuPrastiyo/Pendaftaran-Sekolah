        <h3>PENERIMAAN PESERTA DIDIK BARU (PPDB)</h3>
        <h3>SD Negeri Ceria 2024/2025</h3>
        <p>SD Negeri Ceria pada Tahun Pelajaran 2024/2025 menerima Peserta Didik Baru (PDB) dengan ketentuan sebagai berikut:</p><br>
    <h4>I. DASAR</h4><br>
    <ol>
      <li>Undang-Undang Nomor 20 Tahun 2003 tentang Sistem Pendidikan Nasional.</li>
      <li>Peraturan Pemerintah Nomor 19 Tahun 2005 tentang Standar Nasional Pendidikan.</li>
      <li>Peraturan Menteri Pendidikan Nasional RI Nomor 34 Tahun 2006 tentang Pembinaan Prestasi Peserta Didik yang memiliki potensi kecerdasan dan atau bakat istimewa.</li>
      <li>Peraturan Menteri Pendidikan dan Kebudayaan RI Nomor 64 Tahun 2013 tentang Standar Isi Pendidikan Dasar dan Menengah.</li>
      <li>Peraturan Menteri Pendidikan dan Kebudayaan RI Nomor 103 Tahun 2013 tentang Standar Proses.</li>
      <li>Peraturan Bersama antara Menteri Pendidikan dan Kebudayaan RI dan Menteri Agama RI Nomor 2/VII/PB/2014 dan Nomor 7 Tahun 2014 tentang Penerimaan Peserta Didik Baru pada Taman Kanak-Kanak/Raudhatul Athfal/Bustanul Athfal dan Sekolah/Madrasah.</li>
    </ol><br>

    <h4>II. Jalur Masuk</h4>
      <p><strong>1. Jalur Reguler (Umum).</strong></p>
      <p><strong>2. Alur Non Reguler, yang meliputi : </strong></p><br>

      <p><strong>a. Tidak Mampu/Miskin (TM)</strong> </p>
      <p>Jalur tidak mampu/miskin diperuntukkan bagi calon peserta didik baru yang berasal dari keluarga tidak mampu/miskin,
         dibuktikan adanya kepemilikan Kartu Keluarga Sejahtera (KKS)/Kartu Indonesia Pintar (KIP).</p><br>

      <p><strong>b. Anak Berkebutuhan Khusus (ABK)</strong> </p>
      <p>Jalur ABK diperuntukkan bagi calon peserta didik baru berkebutuhan khusus (tunanetra, tunarungu, tunawicara, tunagrahita, tunadaksa, tunalaras, tunaganda, 
         berkesulitan belajar, lamban belajar, autis, memiliki gangguan motorik, dan memiliki kelainan lainnya).</p><br>

      <h4>III. Syarat - Syarat Pendaftaran</h4><br>
        <ol>
          <li>Calom peserta didik baru kelas 1 SD harus berusia 7 tahun,
              paling rendah 6 tahun pada tanggal 1 Juni tahun berjalan.</li>
          <li>Dalam pelaksanaan PPDB, SD memprioritaskan penerimaan calon peserta didik baru kelas 1 (satu) SD yang berusia 7 (tujuh) tahun.</li>
          <li>Persyaratan usia paling rendah sebagaimana dimaksud pada ayat (1) huruf b dapat dikecualikan menjadi paling rendah 5 (lima) tahun 6 (enam) bulan pada tanggal 1 Juli tahun berjalan 
              bagi calon peserta didik yang memiliki : </li>
              <p>a. Kecerdasan dan/atau bakat istimewa; dan</p>
              <p>b. Kesiapan Psikis.</p>
          <li>Calon peserta didik yang memiliki kecerdasan dan/atau bakat istimewa dan kesiapan psikis sebagaimana dimaksud pada ayat (3) dibuktikan dengan rekomendasi tertulis dari psikolog profesional.</li>
          <li>Dalam hal psikolog profesional sebagaimana dimaksud pada ayat (4) tidak tersedia, rekomendasi dapat dilakukan oleh dewan guru sekolah yang bersangkutan.</li>
        </ol><br>

      <h4>IV. Tata Cara Pendaftaran </h4>
      <p>Pendaftaran calon peserta didik baru SD Negeri Ceria pada Tahun Pelajaran 2024/2025 di lakukan secara ONLINE</p><br>
        <ol>
          <li>Langkah 1</li>
          <p>Calon peserta didik baru mengisi formulir dan mencetak kartu pendaftaran secara online mulai tanggal 25 s.d 27 Juni 2018 melalui website: 
             www.ppdb.sdnceria.sch.id. Pengisian formulir secara online ditutup pada tanggal 27 Juni 2024, Pukul 11.00 WIB.</p>
          <li>Langkah 2</li>
          <p>Calon peserta didik baru menyerahkan berkas pendaftaran ke panitia PPDB SD Negeri Ceria dengan memakai seragam sekolah asal dari tanggal 25 s.d 27 Juni 2024
            (Pukul 08.00 s.d 13.00 WIB) untuk diverifikasi lebih lanjut oleh panitia.</p>
        </ol><br>

        <ol>
