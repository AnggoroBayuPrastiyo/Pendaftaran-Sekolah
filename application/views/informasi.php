        <h4>V. Ketentuan Seleksi </h4>
      <p>Setiap calon peserta didik baru (PDB) wajib mengikuti seleksi yang diselenggarakan oleh panitia Penerimaan Peserta Didik Baru (PPDB) SD Negeri Ceria dengan ketentuan sebagai berikut:</p><br>
        <ol>
          <li>Seleksi Administrasi</li>
          <p>- Seleksi administrasi yakni verifikasi kelengkapan berkas sesuai dengan syarat administrasi seperti yang termuat dalam syarat umum dan khusus.</p>
          <p>- Bagi calon peserta didik yang mendaftar melalui jalur non reguler (TM dan ABK), apabila lolos seleksi administrasi maka calon peserta didik dinyatakan langsung</p><br>

          <li>Seleksi Nilai</li>
          <p>- Seleksi Nilai disini terdiri atas 2 poin yakni Nilai Tes Potensi Akademik (TPA) serta nilai penskoran jarak antara rumah calon peserta didik dengan sekolah. 
               <br>Seleksi nilai dilakukan untuk meranking calon peserta didik baru yang mendaftar melalui jalur reguler setelah calon peserta didik tersebut dinyatakan lolos seleksi administrasi. </p>
        </ol><br>

        <h4>VI. Pengumuman Hasil Seleksi dan Daftar Ulang </h4>
